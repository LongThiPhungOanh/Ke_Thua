package BaiTap.Circle_Cylinder;

public class Circle {
    private double radius;
    private String color;
    public void setRadius(double radius) {
        this.radius = radius;
    }
    public double getRadius() {
        return radius;
    }
    public void setColor(String color) {
        this.color = color;
    }
    public String getColor() {
        return color;
    }

}
